package org.jwonkafx.model;
public class Marca
{
    private int IdMarca;
    private String Nombre;
    public int getIdMarca()
    {
        return IdMarca;
    }
    public void setIdMarca(int IdMarca)
    {
        this.IdMarca = IdMarca;
    }
    public String getNombre()
    {
        return Nombre;
    }
    public void setNombre(String Nombre)
    {
        this.Nombre = Nombre;
    }
}
